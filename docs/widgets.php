<h3>Widgets</h3>
<p>On this theme it has around 4 different custom useful widgets as well which all you can use from your widgets page with some wide settings for each of them.</p>

<ol>
<li><p><strong>Mondira Custom Image:</strong> It a custom widget to display custom image on sidebar widget area. When select a image you can browse that image from wp media with a pop-up just like you do when using featured image for a blog post. You can also link it with any external link.</p></li>
<li><p><strong>Mondira Flickr:</strong> A custom widget to show a set of images from your flickr account on sidebar widget area. You can configure how many images you want to display.</p></li>
<li><p><strong>Mondira Twitter:</strong> A custom widgets to display twitter latest posts on footer sidebar widgets area. </p></li>
<li><p><strong>Mondira Video:</strong> A custom widgets to display youtube / vimeo video on sidebar widgets area. </p></li>
</ol>