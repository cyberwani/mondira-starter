<h3>Importing the Sample Content</h3>
<p>Importing the sample content is easy. Go to Tools > Import and select the last option called WordPress.</p>

<p>If the Importer is not installed, a pop-up window will appear asking you to install it.</p>

<p>Click the blue Install now button on the right and click Activate Plugin & Run Importer.</p>

<p>Click Browse and select the Mondira_sample_content.xml Mondira_sample_content file located in the Wordpress/Sample Content sub folder and click Upload file and import. Check the Download and import file attachements. Click Submit.</p>

